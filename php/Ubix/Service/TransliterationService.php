    /**
     * Transliterate characters to English alphabet letters.
     *
     * This code snippet handles the transliteration of characters from various languages to their corresponding English alphabet letters. It suggests that a better approach would be to use the `iconv` or `intl` extensions for more accurate transliteration.
     *
     * The code references a list of letters from Latvian, Lithuanian, Russian, Czech, Polish, Portuguese, German, and French panagrams, which can be found at the following link: [https://clagnut.com/blog/2380/](https://clagnut.com/blog/2380/)
     *
     * @param string $string                          The string to transliterate
     * @param bool   $transliterateCommonCharacters   Whether or not to transliterate common characters
     * @param bool   $transliterateOddCharacters      Whether or not to transliterate odd characters
     * @param bool   $transliterateCyrillicCharacters Whether or not to transliterate Cyrillic characters
     *
     * @return string The transliterated string
     */
    public function transliterate(
        string $string,
        bool $transliterateCommonCharacters = true,
        bool $transliterateOddCharacters = true,
        bool $transliterateCyrillicCharacters = true,
    ): string {
        //
        //  Transliterate the string
        //
        $transliteratedString = $string;

        if ($transliterateCommonCharacters) {
            $transliteratedString = str_replace(self::CHARACTERS_COMMON_BEFORE, self::CHARACTERS_COMMON_AFTER, $transliteratedString);
        }

        if ($transliterateOddCharacters) {
            $transliteratedString = str_replace(self::CHARACTERS_ODD_BEFORE, self::CHARACTERS_ODD_AFTER, $transliteratedString);
        }

        if ($transliterateCyrillicCharacters) {
            $transliteratedString = str_replace(self::CHARACTERS_CYRILLIC_BEFORE, self::CHARACTERS_CYRILLIC_AFTER, $transliteratedString);
        }

        //
        //  Return the string in title case
        //
        return ucwords(strtolower($transliteratedString));
    }
